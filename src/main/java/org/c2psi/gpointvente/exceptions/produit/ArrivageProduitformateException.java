package org.c2psi.gpointvente.exceptions.produit;

public class ArrivageProduitformateException extends Exception {
    public ArrivageProduitformateException() {
    }

    public ArrivageProduitformateException(String message) {
        super(message);
    }

    public ArrivageProduitformateException(String message, Throwable cause) {
        super(message, cause);
    }

    public ArrivageProduitformateException(Throwable cause) {
        super(cause);
    }

    public ArrivageProduitformateException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
